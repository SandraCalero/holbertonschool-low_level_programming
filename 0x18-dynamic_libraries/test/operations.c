#include "main.h"
/**
 * add - adds two numbers
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - subs two numbers
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplies two numbers
 */
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - divides two numbers
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - module of two numbers
 */
int mod(int a, int b)
{
	return (a % b);
}
