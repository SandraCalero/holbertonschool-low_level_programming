#include "3-calc.h"
/**
 * op_add - sums two integers
 * @a: first number to sum
 * @b: second number to sum
 *
 * Return: The result of a sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtracts two integers
 * @a: first number to subtract
 * @b: second number to subtract
 *
 * Return: The result of a subtraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies two integers
 * @a: first number to multiply
 * @b: second number to multiply
 *
 * Return: The result of a multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two integers
 * @a: first number to divide
 * @b: second number to divide
 *
 * Return: The result of a division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - calculates the remainder of a division
 * @a: dividend
 * @b: divisor
 *
 * Return: The remainder of the division
 */
int op_mod(int a, int b)
{
	return (a % b);
}
